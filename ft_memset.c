#include <stdlib.h>
void	*ft_memset(void *t, int c, size_t n)
{
	unsigned int		i;
	char				*s;

	s = t;
	i = 0;
	while (i < n)
	{
		s[i] = c;
		i++;
	}
	return (s);
}
