#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	n;

	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = nb * -1;
		write(fd, "-", 1);
	}
	n = nb % 10 + '0';
	if (nb >= 10)
	{
		nb = nb / 10;
		ft_putnbr_fd(nb, fd);
	}
	write(fd, &n, 1);
}
