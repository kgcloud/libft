#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t	i;
	char	*s;
	char	*t;

	i = 0;
	s = (char*)dest;
	t = (char*)src;
	while (i < n && t[i] != c)
		i++;
	if (s[i] == c)
		return (&s[i + 1]);
	return (0);
}
