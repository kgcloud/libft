#include "libft.h"

int		ft_isalnum(int c)
{
	if (c >= 'a' && 'z' >= c)
		return (1);
	if (c >= 'A' && 'Z' >= c)
		return (1);
	if (c >= '0' && '9' >= c)
		return (1);
	return (0);
}
