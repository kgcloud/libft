#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	i;
	char			*tab;

	i = 0;
	while (s1[i])
		i++;
	tab = malloc(sizeof(*tab) * (i + 1));
	if (tab == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
